#!/usr/bin/env python
"""
Launch all HPO experiments generated by generate_experiments.py.

Reads runs_manifest.json and either:
  - Launches training runs locally (sequentially or with multiprocessing)
  - Generates and submits LSF job array for HPC
"""

import sys
import os
import json
import argparse
import subprocess
from pathlib import Path
from tqdm import tqdm

from helpers.paths import load_config, PROJECT_ROOT
from helpers.parallel import BetterPool


def make_argument_parser(argv):
    parser = argparse.ArgumentParser(
        prog=argv[0],
        description="Launch HPO experiments (locally or on HPC)"
    )
    parser.add_argument(
        "experiment_config",
        type=str,
        help="Path to experiment_config.json"
    )
    parser.add_argument(
        "--hpc",
        action="store_true",
        default=False,
        help="Submit to HPC (LSF) instead of running locally"
    )
    parser.add_argument(
        "--processes",
        type=int,
        default=1,
        help="Number of parallel processes for local execution (default: 1)"
    )
    parser.add_argument(
        "--dry-run",
        action="store_true",
        default=False,
        help="Print commands without executing"
    )
    parser.add_argument(
        "--run-key",
        default=None,
        type=str,
        help="Specify just a single run to execute"
    )
    return parser


def launch_run_local(run_info, dry_run=False):
    """Launch a single training run locally."""
    run_dir = Path(run_info["run_dir"])
    run_name = Path(run_dir).name
    train_script = PROJECT_ROOT / "training" / "roi_train2" / "train.py"

    # Change to run directory and run train.py
    cmd = f"cd {run_dir} && python {train_script} --run-dir {run_dir}"

    if dry_run:
        print(f"[DRY RUN] {run_name}: {cmd}")
    else:
        print(f"Launching {run_name}...")
        try:
            subprocess.run(cmd, shell=True, check=True)
            print(f"  ✓ {run_name} completed")
        except subprocess.CalledProcessError as e:
            print(f"  ✗ {run_name} FAILED: {e}")
            raise


def launch_local(manifest_dict, dry_run=False, processes=1, run_key=None):
    """Launch all runs locally."""
    if run_key is None:
        runs = list(manifest_dict.values())
    else:
        runs = [manifest_dict[run_key]]

    if processes == 1:
        # Sequential execution
        for run_info in tqdm(runs):
            launch_run_local(run_info, dry_run=dry_run)
    else:
        # Parallel execution
        with BetterPool(processes) as pool:
            tasks = [
                {"run_info": run_info, "dry_run": dry_run} for run_info in runs
            ]
            results = pool.imap_unordered(
                lambda task: launch_run_local(**task),
                tasks
            )
            for _ in tqdm(results, total=len(runs)):
                pass


def generate_lsf_array_script(training_work_home, manifest_dict, output_script):
    """Generate an LSF job array submission script for UMass Chan SCI Cluster."""
    runs = list(manifest_dict.values())

    script_content = f"""#!/bin/bash
#BSUB -J "prl_hpo[1-{len(runs)}]"
#BSUB -n 1
#BSUB -q gpu
#BSUB -gpu "num=1"
#BSUB -R "rusage[mem=32G]"
#BSUB -W 24:00
#BSUB -o {training_work_home}/logs/run_%J_%I.log
#BSUB -e {training_work_home}/logs/run_%J_%I.err

# Source HPC environment setup
source {PROJECT_ROOT}/hpc/setup_env_hpc.sh

echo "Host: $(hostname)"
echo "GPU: $(nvidia-smi --query-gpu=name --format=csv,noheader 2>/dev/null || echo 'N/A')"
echo "Array index: $LSB_JOBINDEX"

# Map array index to run directory
runs=(
"""
    for run_info in runs:
        script_content += f'    "{run_info["run_dir"]}"\n'

    script_content += f""")

run_dir=${{runs[$LSB_JOBINDEX - 1]}}

echo "Starting training in $run_dir"
cd "$run_dir"
python {PROJECT_ROOT}/training/roi_train2/train.py --run-dir "$run_dir"
echo "Training in $run_dir completed with exit code $?"
"""

    with open(output_script, "w") as f:
        f.write(script_content)

    os.chmod(output_script, 0o755)
    print(f"Generated LSF submission script: {output_script}")


def submit_to_hpc(manifest_path, dry_run=False):
    """Submit all runs to HPC as a job array."""
    manifest_path = Path(manifest_path)
    training_work_home = manifest_path.parent

    with open(manifest_path) as f:
        manifest_dict = json.load(f)

    # Generate the job array script
    submit_script = training_work_home / "submit_array.sh"
    generate_lsf_array_script(training_work_home, manifest_dict, submit_script)

    # Create logs directory
    logs_dir = training_work_home / "logs"
    logs_dir.mkdir(exist_ok=True)

    if dry_run:
        print(f"[DRY RUN] Would submit: bsub < {submit_script}")
    else:
        try:
            subprocess.run(f"bsub < {submit_script}", shell=True, check=True)
            print(f"✓ Submitted {len(manifest_dict)} jobs to HPC")
        except subprocess.CalledProcessError as e:
            print(f"✗ Failed to submit to HPC: {e}")
            raise


def main(argv=None):
    if argv is None:
        argv = sys.argv

    parser = make_argument_parser(argv)
    args = parser.parse_args()

    run_home = Path(args.experiment_config).parent

    # Load experiment config to find manifest
    exp_config = load_config(args.experiment_config)
    exp_name = exp_config.get("experiment_name", "hpo_experiment")
    base_monai_config = load_config(run_home/exp_config["base_monai_config"])
    training_work_home = Path(base_monai_config["training_work_home"]) / exp_name
    manifest_path = training_work_home / "runs_manifest.json"

    if not manifest_path.exists():
        print(f"ERROR: {manifest_path} not found.")
        print(f"Run generate_experiments.py first to create configs.")
        sys.exit(1)

    with open(manifest_path) as f:
        manifest_dict = json.load(f)

    print(f"Found {len(manifest_dict)} runs to execute")

    if args.hpc:
        submit_to_hpc(manifest_path, dry_run=args.dry_run)
    else:
        launch_local(manifest_dict, dry_run=args.dry_run, processes=args.processes, run_key=args.run_key)

    print("Done!")


if __name__ == "__main__":
    main()
